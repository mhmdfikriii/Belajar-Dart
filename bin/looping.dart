void main() {
  print('======FOR======');
  for (var counter = 1; counter <= 10; counter++) {
    print('Hello World');
  }
  print('======WHILE======');
  var counter = 1;

  while (counter <= 10) {
    print('HALLO');

    counter++;
  }
  print('======DO WHILE======');

  var i = 1;

  do {
    print('HAII');
    i++;
  } while (i <= 10);
}
